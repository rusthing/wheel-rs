/// # 命令执行错误枚举
/// 
/// 该枚举包含了执行外部命令时可能遇到的各种错误类型。
/// 使用 thiserror crate 提供错误信息的自动实现。
#[derive(Debug, thiserror::Error)]
pub enum CmdError {
    /// 命令执行失败错误
    /// 
    /// 当无法启动命令或在设置命令执行环境时发生错误时返回此变体。
    /// 包装了底层的 [`std::io::Error`]。
    #[error("命令执行失败: {0}")]
    ExecuteFail(std::io::Error),
    
    /// 命令运行失败错误
    /// 
    /// 当命令被执行但返回非零退出码时返回此变体。
    /// 包含命令输出的错误信息。
    #[error("命令运行失败: {0}")]
    RunFail(String),
}